package com.example.auth_service.dto;

import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Pattern;
import lombok.Data;

/**
 * DTO для запроса на сброс пароля.
 * Используется при подтверждении смены пароля с помощью токена, отправленного на email.
 */
@Data
public class PasswordResetDto {

    /**
     * Токен, выданный пользователю для подтверждения сброса пароля.
     * Обычно отправляется на email при запросе на сброс пароля.
     */
    @NotBlank(message = "Токен не может быть пустым")
    private String token;

    /**
     * Новый пароль пользователя.
     * Пароль должен соответствовать требованиям безопасности:
     * минимум 8 символов, хотя бы одна цифра, одна строчная буква,
     * одна заглавная буква и один специальный символ.
     */
    @NotBlank(message = "Новый пароль не может быть пустым")
    @Pattern(
            regexp = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[!@#$%^&*()_+\\-=\\[\\]{}|;:,.<>?]).{8,}$",
            message = "Пароль должен содержать минимум 8 символов, включая заглавные и строчные буквы, цифры и специальные символы"
    )
    private String newPassword;
}
